package com.company;

public class Main {

    public static void main(String[] args) {

        int var1 = 50; // int var1 = 50; is a statement

        // these are statements as well
        var1++;
        var1--;

        System.out.println("This is a test"); // entire line is a statement

        // Statements do not need to be in one line
        System.out.println("This is" +
                " another" +
                " still more.");

        int var2 = 50; var1--; // two statements in one line, not recommended


    }
}
